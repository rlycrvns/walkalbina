      <p>Albina is a neighborhood located in North Portland, Oregon. For most of the 20th century, it was home to the majority of the city’s African American population. Located across the Willamette River from downtown Portland, Albina was once one of the fastest growing cities in Oregon until it was annexed by the City of Portland in July of 1891. The Albina neighborhood was a place where African American families could live and work until the start of World War II. The shipyards along the Columbia River and the draw of other war time jobs brought an influx of workers from around the country, giving significant rise to population between 1940 and 1945. </p>
      <p>By the beginning of the 1950s, unable to find jobs in white businesses, residents began to open their own stores, restaurants and clubs to serve the African American community. In 1956 Albina was declared a blight, with least 60% of the housing considered to be sub-standard.</p>
      <p>By the 1960’s, despite best efforts to rehabilitate structures, landlords in Albina began to evict tenants, demolish buildings, and sell off the land. By the 1970s, over half of that neighborhood’s black population was forced to relocate, leaving behind a neighborhood rich in history and cultural significance.</p>
      <p>Join Dr. Andrea Lowgren and the students of Portland Community College’s History 251 Course&mdash;African-American History Since 1877&mdash;as they host a walking tour of six historic locations in the Albina neighborhood of Portland, Oregon. While on the walk, a short presentation will be given about each stop exploring the rich cultural history of the area and the individuals who lived there. </p>
